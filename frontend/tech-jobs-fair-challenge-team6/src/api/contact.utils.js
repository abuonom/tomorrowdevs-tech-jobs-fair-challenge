export const formatContactData = (contactsData) => {
  const formatedData = {
    data: convertContactDataIntoFrontEndType(contactsData.data),
    curentPage: contactsData.current_page,
    total: contactsData.total,
  };
  return formatedData;
};

const convertContactDataIntoFrontEndType = (data) => {
  const convertedContactData = data.map((item) => {
    return {
      access: item.access,
      // dateOfBirth: item.date_of_birth?.toString(),
      email: item.email,
      id: item.id,
      name: item.name,
      note: item.note,
      number: item.number,
      profileImg: item.profile_picture,
      socialMediaURL: item.social_media_url,
      surname: item.surname,
      type: item.type_id,
      userId: item.user_id,
      department: item.department_id?.toString(),
    };
  });
  return convertedContactData;
};

export const convertContactDataIntoBackEndType = (data) => {
  const convertedContactData = {
    access: data.access,
    date_of_birth: data.dateOfBirth,
    email: data.email,
    id: data.id,
    name: data.name,
    note: data.note,
    number: data.number,
    profile_picture: data.profileImg,
    social_media_url: data.socialMediaURL,
    surname: data.surname,
    type_id: data.type,
    department_id: data.department,
  };
  return convertedContactData;
};

export const prepareQueryFilterParam = (filterParams) => {
  const newFilterParams = {
    ...filterParams,
  };
  if (filterParams.department) {
    newFilterParams.department_ids = filterParams.department.map(Number);
    delete newFilterParams.department;
  }
  // if (filterParams.contactType) {
  //   newFilterParams.type_ids = filterParams.contactType.map(Number);
  //   delete newFilterParams.contactType;
  // }
  return newFilterParams;
};

